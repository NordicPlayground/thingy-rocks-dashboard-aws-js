import { normalizePositions } from './normalizePositions'

describe('normalizePositions()', () => {
	it('should move all positions in the list, so they are all positive', () =>
		expect(
			normalizePositions([
				{
					x: 0,
					y: -74,
				},
				{
					x: 74.27399083446284,
					y: -59.23153117657969,
				},
				{
					x: 26.32305362890924,
					y: 6.008065216820487,
				},
				{
					x: 12.148744695291631,
					y: 25.227128301267733,
				},
				{
					x: -22.561954434113016,
					y: 46.8503811309258,
				},
				{
					x: -32.17262110200018,
					y: 7.343190820558381,
				},
				{
					x: -59.41919266757027,
					y: -47.385224941263736,
				},
			]),
		).toMatchObject({
			elements: [
				{ x: 59.41919266757027, y: 0 },
				{ x: 133.6931835020331, y: 14.768468823420307 },
				{ x: 85.74224629647951, y: 80.00806521682048 },
				{ x: 71.5679373628619, y: 99.22712830126773 },
				{ x: 36.85723823345725, y: 120.8503811309258 },
				{ x: 27.246571565570093, y: 81.34319082055838 },
				{ x: 0, y: 26.614775058736264 },
			],
			center: {
				x: 59.41919266757027,
				y: 74,
			},
		}))
})
